// 실습 15 - 중첩 for문으로 구구단 전체 출력하기 [정답]
// =========================================

for (let dan = 2; dan <= 9; dan++) {
    console.log('===', dan + '단', '===');
    for (let i = 1; i <= 9; i++) {
        console.log(dan + ' x ' + i + ' = ' + (dan * i));
    }
    console.log('');
}
