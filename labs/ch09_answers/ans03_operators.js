console.log('① 10 > 5          :', 10 > 5);           // 예상: true
console.log('② 10 === "10"     :', 10 === '10');       // 예상: false
console.log('③ 10 == "10"      :', 10 == '10');        // 예상: true
console.log('④ (7>3)&&(2<1)    :', (7>3) && (2<1));   // 예상: false
console.log('⑤ (7>3)||(2<1)    :', (7>3) || (2<1));   // 예상: true
console.log('⑥ !(10===10)      :', !(10 === 10));      // 예상: false
